#include <stdio.h>
void ft_swap(int *a, int *b)
{
    int aux;
    aux = *a;
    *a = *b;
    *b = aux;
}

int particiona(int *v, int inicio, int fim)
{
    int meio_pivo;
    int aux;

    meio_pivo = (v[inicio] + v[fim] + v[(inicio + fim) / 2]) / 3;
    while(inicio < fim)
    {
        while (inicio < fim && v[inicio] <= meio_pivo)
        {
            inicio++;
        }
        while (inicio < fim && v[fim] > meio_pivo)
        {
            fim--;
        }
        ft_swap(&v[inicio], &v[fim]);
    }
    return(inicio);
}

void quick_sort(int *v, int inicio, int fim)
{
    
    if(inicio < fim){
        int posicao;
        posicao = particiona(v,inicio,fim);
        quick_sort(v, inicio, posicao -1);
        quick_sort(v, posicao, fim);
    }
        
}
